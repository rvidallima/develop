// tipos primitivos de dados

// string
let saudacao = 'helo, world!'
console.log(saudacao)
console.log(typeof saudacao, 'string')
// number
let age = 27
console.log(age)
console.log(typeof age, 'number')
let price = 19.99
console.log(price)
console.log(typeof price, 'number com ponto flutuante')
// boolean
let ativo = true
console.log(ativo)
console.log(typeof ativo, 'boolean')
let desligado = false
console.log(desligado)
console.log(typeof desligado, 'boolean')
// null
let resultado = null
console.log(resultado)
console.log(typeof resultado, 'null')
// undefined
let status
console.log(status)
console.log(typeof status, 'undefined')